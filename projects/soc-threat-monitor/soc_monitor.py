# ─────────────────────────────────────────────────────────────────────────────
# Main
# ─────────────────────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(
        description="SOC Threat Detection Monitor",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  python soc_monitor.py --demo
  python soc_monitor.py --log /var/log/auth.log
  python soc_monitor.py --log auth.log --output alerts.json
        """,
    )
    parser.add_argument("--log", help="Path to auth log file")
    parser.add_argument("--rules", help="Path to rules JSON file", default=None)
    parser.add_argument("--output", help="Save alerts to JSON file", default=None)
    parser.add_argument("--demo", action="store_true", help="Run with built-in demo data")
    args = parser.parse_args()

    rules = None
    if args.rules:
        with open(args.rules) as f:
            rules = json.load(f)

    if args.demo:
        print("[*] Running SOC Monitor in DEMO mode...")
        lines = DEMO_LOG.strip().split("\n")
        events = [parse_log_line(line) for line in lines]
        events = [e for e in events if e]
    elif args.log:
        log_path = Path(args.log)
        if not log_path.exists():
            print(f"[!] Log file not found: {log_path}")
            sys.exit(1)
        print(f"[*] Parsing log file: {log_path}")
        events = parse_log_file(str(log_path))
    else:
        parser.print_help()
        sys.exit(0)

    print(f"[+] Parsed {len(events)} log events.")
    engine = DetectionEngine(rules)
    alerts = engine.process_events(events)
    print(f"[+] Detection complete. {len(alerts)} alert(s) generated.")

    print_report(alerts, len(events))

    if args.output:
        output_path = Path(args.output)
        alert_data = [a.to_dict() for a in alerts]
        with open(output_path, "w") as f:
            json.dump(alert_data, f, indent=2)
        print(f"[+] Alerts saved to: {output_path}")


if __name__ == "__main__":
    main()
